package lacos_de_repeticao;

public class exercicio3_for {

	public static void main(String[] args) {
		// exibição soma pares
int soma = 0;
		
        for(int i = 2; i<= 100; i++) {
        		soma += i;
        		i+=1;
        }
        System.out.print("A soma do números pares de 1 a 100 é: "+ soma);
      

	}

}
